#!/bin/bash
"""
ТЕСТИРОВАНИЕ РАЗНЫХ ПОРТОВ
"""

echo "================================================================================"
echo "🔍 ТЕСТИРОВАНИЕ ПОРТОВ И ПРОТОКОЛОВ"
echo "================================================================================"
echo

# Тестируемые порты
PORTS=(80 443 2053 8443 9443 10443)

echo "🔍 Тестируемые порты: ${PORTS[@]}"
echo

# Проверяем, какие порты слушаются
echo "1️⃣  Проверка, какие порты слушаются Xray:"
for port in "${PORTS[@]}"; do
    result=$(ss -tlnp | grep ":$port")
    if [ -n "$result" ]; then
        echo "   ✅ Порт $port: слушается"
        echo "      $result"
    else
        echo "   ❌ Порт $port: НЕ слушается"
    fi
done
echo

# Проверяем правила UFW
echo "2️⃣  Проверка правил UFW:"
ufw status numbered
echo

# Проверяем iptables для этих портов
echo "3️⃣  Проверка iptables для портов:"
for port in "${PORTS[@]}"; do
    result=$(iptables -L INPUT -n -v | grep "dpt:$port")
    if [ -n "$result" ]; then
        echo "   Port $port: $result"
    else
        echo "   Port $port: нет явных правил"
    fi
done
echo

# Попытка создать локальное соединение
echo "4️⃣  Тест локального соединения:"
for port in "${PORTS[@]}"; do
    result=$(timeout 2 bash -c "echo >/dev/tcp/127.0.0.1/$port" 2>&1)
    if [ $? -eq 0 ]; then
        echo "   ✅ Port $port: локальное соединение успешно"
    else
        echo "   ❌ Port $port: локальное соединение НЕ удалось"
    fi
done
echo

echo "================================================================================"
echo "✅ ТЕСТИРОВАНИЕ ЗАВЕРШЕНО"
echo "================================================================================"
echo
echo "💡 РЕКОМЕНДАЦИИ:"
echo "   1. Если порт 443 НЕ слушается — запустите на нем VLESS/TLS (без Reality)"
echo "   2. Если фаервол хостинга блокирует — откройте порты в панели хостинга"
echo "   3. Попробуйте сменить IP сервера (создайте новый VPS)"
echo
